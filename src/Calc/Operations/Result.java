package Calc.Operations;

class Result {

    int incrementNums (int a, int b){
        return a + b;
    }
    int decrementNums (int a, int b){
        return a - b;
    }
    int divisionNums (int a, int b){

        return a / b;
    }
    int multipleNums (int a, int b){
        return a * b;
    }
}
